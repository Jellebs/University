class Galgemand: 
    __ordListe: Ordliste
    display: galgemand_Display = galgemand_Display().Udseende
    spilØnskes: bool = True
    erSpilAktivt: bool = True

    __hemmeligtOrd = []
    gæt: list[str] = []
    __fejlGæt = []
    __gætAntal: int = 5


    #Setup logik
    def __init__(self, ordliste: Ordliste):
        self.__ordListe = ordliste

    def gætInit(self): 
        self.gæt = []
        for i in range(len(self.__hemmeligtOrd)):
            if self.__hemmeligtOrd[i] == " ": 
                self.gæt.append(" ")
            else: 
                self.gæt.append("x")

    #Samme format globalt, så strengene kan sammenlignes
    def strengFormat(self, streng: str) -> str:
        return streng.strip().upper()

    #Streng - List converter og tilbage 
    def strengFraListe(self, liste) -> str: 
        return ''.join(liste)
    
    def listeFraStreng(self, streng: str) -> list[str]:
        format: str = self.strengFormat(streng)
        list: list[str] = []
        for bogstav in format: 
            list.append(bogstav)
        return list

    
