    def spil(self): 
    
        #Hjælpefunktioner: 
        def vundet(): 
            print("Hurra du har vundet! Snemanden får lov til at leve endnu et spil")
            print(f"Ordet var nemlig {self.strengFraListe(self.__hemmeligtOrd)} ")
            self.erSpilAktivt = False
            fåReaktion()
            genstartSpil()

        def tabt(): 
            print("Det klarede du dårligt, snemanden døde.")
            print(f"Det rigtige ord var { self.strengFraListe(self.__hemmeligtOrd)}")
            self.erSpilAktivt = False
            fåReaktion()
            genstartSpil()
        
        def genstartSpil(): 
            self.__fejlGæt = []
            self.gætInit()

        def fåReaktion():
            print("Ønsker du at prøve igen? Skriv \"Ja\" for at fortsætte, eller \"Nej\" for at afslutte.")
